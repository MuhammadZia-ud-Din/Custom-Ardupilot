/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  /* Relocate vector table to 0x08010000 (bootloader leaves VTOR at 0x08000000).
   * Must happen before HAL_Init() enables SysTick. */
  SCB->VTOR = 0x08010000UL;
  __DSB();
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  MX_TIM6_Init();
  /* USER CODE BEGIN 2 */

  /* Start TIM6 as free-running counter (100 kHz, 10 µs/tick).
   * No interrupt needed – HAL_GetTick() is used for all timing. */
  HAL_TIM_Base_Start(&htim6);

  /* Initialise DroneCAN node: filter, CAN start, canard init, node ID=14 */
  dronecan_init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    /* DroneCAN: broadcast NodeStatus every 1 s, drain TX queue */
    dronecan_process();

    /* PD12 green LED: 500 ms on / 500 ms off = 1 s blink period */
    uint32_t now = HAL_GetTick();
    if ((now - g_led_last_toggle_ms) >= 500U)
    {
      g_led_last_toggle_ms = now;
      g_led_state ^= 1U;
      HAL_GPIO_WritePin(LED_PORT, LED_PIN,
                        g_led_state ? GPIO_PIN_SET : GPIO_PIN_RESET);
    }

    /* ---------------------------------------------------------------
     * Hardware watchdog (IWDG) pat.
     * The ArduPilot bootloader calls stm32_watchdog_init() before
     * jumping to the application, starting the IWDG with ~30 s timeout.
     * Writing 0xAAAA to IWDG_KR reloads the counter every loop so it
     * never expires.  If we omit this and the watchdog fires, the
     * bootloader will see (was_watchdog=TRUE, BKP0R != RTC_BOOT_FWOK)
     * on the next power-on and stay in bootloader mode permanently.
     * --------------------------------------------------------------- */
    IWDG->KR = 0xAAAAU;

    /* ---------------------------------------------------------------
     * Set RTC_BOOT_FWOK (0x890b4f89) once after 5 s of healthy uptime.
     * Value must match RTC_BOOT_FWOK in AP_HAL_ChibiOS watchdog.h.
     * This tells the bootloader: "this firmware ran OK – safe to boot
     * again after the next watchdog reset."  Without it, a watchdog
     * reset after power-on would lock the board in the bootloader.
     * --------------------------------------------------------------- */
    if ((now >= 5000U) && !g_fwok_set)
    {
      g_fwok_set = 1U;
      /* Enable RTC backup domain write access (same sequence as
       * rtc_write_bkp0 in dronecan_node.c). */
      RCC->APB1ENR |= RCC_APB1ENR_PWREN;
      PWR->CR      |= PWR_CR_DBP;
      RCC->BDCR    |= RCC_BDCR_RTCEN;
      RTC->BKP0R    = 0x890b4f89UL;   /* RTC_BOOT_FWOK */
    }

    /* USER CODE END 3 */
  }
  /* USER CODE END 3 */
}

